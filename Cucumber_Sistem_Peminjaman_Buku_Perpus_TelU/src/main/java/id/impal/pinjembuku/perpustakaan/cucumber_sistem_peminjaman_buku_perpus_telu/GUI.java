    private void jTextField1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField1ActionPerformed
        // TODO add your handling code here: 
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        clearTable(model);
        String tanggal = "";
        for (int i = 0; i < anggota.size(); i++) {
            if (anggota.get(i).getNo().contains(jTextField1.getText())) {
                tanggal = anggota.get(anggota.size() - 1).getMassaBerlaku().getDate() + "-" + (anggota.get(anggota.size() - 1).getMassaBerlaku().getMonth() + 1) + "-" + (anggota.get(anggota.size() - 1).getMassaBerlaku().getYear() + 1900);
                model.addRow(new Object[]{anggota.get(i).getNo(), anggota.get(i).getNim(), anggota.get(i).getNama(), tanggal});
            }
        }
    }//GEN-LAST:event_jTextField1ActionPerformed

