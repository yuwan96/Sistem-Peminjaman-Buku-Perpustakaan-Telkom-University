    private void tambahAnggotaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahAnggotaActionPerformed
        // TODO add your handling code here:
        tambahAnggota tambah = new tambahAnggota(this, true);
        tambah.setVisible(true);
        if (tambah.getAng() != null) {
            anggota.add(tambah.getAng());
        }
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        clearTable(model);
        String tanggal = anggota.get(anggota.size() - 1).getMassaBerlaku().getDate() + "-" + (anggota.get(anggota.size() - 1).getMassaBerlaku().getMonth() + 1) + "-" + (anggota.get(anggota.size() - 1).getMassaBerlaku().getYear() + 1900);
        for (int i = 0; i < anggota.size(); i++) {
            model.addRow(new Object[]{anggota.get(i).getNo(), anggota.get(i).getNim(), anggota.get(i).getNama(), tanggal});
        }
    }//GEN-LAST:event_tambahAnggotaActionPerformed

