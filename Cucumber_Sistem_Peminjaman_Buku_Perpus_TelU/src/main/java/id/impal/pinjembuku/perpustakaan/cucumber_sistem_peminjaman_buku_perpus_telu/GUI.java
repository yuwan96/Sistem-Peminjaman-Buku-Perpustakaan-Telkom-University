    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        // TODO add your handling code here:

        int ubah = 0;
        for (int i = 0; i < buku.size(); i++) {
            if (jTable2.getValueAt(jTable2.getSelectedRow(), 0).toString().equals(buku.get(i).getKode())) {
                ubah = i;
                break;
            }
        }
        KelolaBuku edit = new KelolaBuku(this, true, 2, (new Buku(jTable2.getValueAt(jTable2.getSelectedRow(), 0).toString(),
                jTable2.getValueAt(jTable2.getSelectedRow(), 1).toString(),
                jTable2.getValueAt(jTable2.getSelectedRow(), 2).toString(),
                jTable2.getValueAt(jTable2.getSelectedRow(), 3).toString(),
                Integer.parseInt(jTable2.getValueAt(jTable2.getSelectedRow(), 4).toString()))));
        edit.setVisible(true);
        buku.set(ubah, edit.getBuku());
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        clearTable(model);
        for (int i = 0; i < buku.size(); i++) {
            model.addRow(new Object[]{buku.get(i).getKode(), buku.get(i).getJudul(), buku.get(i).getPengarang(), buku.get(i).getPenerbit(), buku.get(i).getJumlah()});
        }
    }//GEN-LAST:event_jButton9ActionPerformed

    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        // TODO add your handling code here:
        int hapus = -1;
        for (int i = 0; i < buku.size(); i++) {
            if (jTable2.getValueAt(jTable2.getSelectedRow(), 0).toString().equals(buku.get(i).getKode())) {
                hapus = i;
                break;
            }
        }
        buku.remove(hapus);
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        clearTable(model);
        for (int i = 0; i < buku.size(); i++) {
            model.addRow(new Object[]{buku.get(i).getKode(), buku.get(i).getJudul(), buku.get(i).getPengarang(), buku.get(i).getPenerbit(), buku.get(i).getJumlah()});
        }
    }//GEN-LAST:event_jButton10ActionPerformed

